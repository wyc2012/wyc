/*********************在72MHz情况下的延时函数*******************************/
void delay_us(uint32_t i)
{
    uint8_t j;
    while (i--)
    {
        for (j = 0; j < 50; j++)
            ;
    }
}
void delay_ms(uint32_t count)
{
    delay_us(count * 1000);
}
